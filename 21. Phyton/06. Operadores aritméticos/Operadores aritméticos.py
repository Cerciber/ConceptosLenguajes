
# Suma
suma = 5 + 3; 

# Resta
resta = 5 - 3; 

# Multiplicación 
multiplicacion = 5 * 3; 

# División entera (los dos valores son enteros) 
divisionEntera = 5 // 3; 

# División racional (Al menos un valor es racional) 
divisionRacional = 5.0 / 3; 

# Modulo 
modulo = 5 % 3;   

# Exponente
exponente = 5 ** 3;   

# Desplazamiento de bits 
desplazamiento_izquierda = 3 << 2;  # Agregar ceros a la derecha a la representación binaria (numero << numero de ceros a agregar)
desplazamiento_derecha = 3 >> 2;   	# Eliminar numeros a la derecha a la representación binaria (numero >> numero de numeros a eliminar)
