package prueba;

public class Prueba {
    
    public static void main(String[] args) {
       
        /* if = realiza la accion si se cumple la condición */
        if (true) {
            /* acción */
        }

        /* if - else = realiza una accion segun el valor de verdad de la condición */
        if (true) {
            /* acción al ser verdadero */
        } else {
                /* acción al ser falso */
        }

        /* if - else - if = realiza realiza una acción segun el valor de verdad de las condiciones */
        if (true) {
            /* acción al ser verdadera la condición 1 */
        } else if(true) {
                /* acción al ser verdadera la condición 2 */
        }else{
                /* acción al ser falsas las condiciónes anteriores */
        }
       
    }
    
}