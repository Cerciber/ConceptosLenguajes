
package prueba;

public class Prueba {

    public static void main(String[] args) {
       
        // AND (Conjunción)
        tipo resultado = expresión1 && expresión2;

        // OR (Disjunción)
        tipo resultado = expresión1 || expresión2;

        // NOT (Negación)
        tipo resultado = !expresión;

        // EQUAL / XNOR (Igualdad)
        tipo resultado = expresión1 == expresión2;

        // NEQUAL / XOR (Diferencia)
        tipo resultado = expresión1 != expresión2;

        // NAND (Negación de la Conjunción)
        tipo resultado = !(expresión1 && expresión2);

        // NOR (Negación de la Disjunción)
        tipo resultado = !(expresión1 || expresión2);

        // IF (Implicación)
        tipo resultado = !expresión1 || expresión2;


    }
    
}
