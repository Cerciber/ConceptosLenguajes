<?php
	
	/* Suma */
	$suma = 5 + 3; 
	echo "5 + 3 = ".$suma."<br>";

	/* Resta */
	$resta = 5 - 3; 
	print("5 - 3 = ".$resta."<br>");

	/* Multiplicación */
	$multiplicacion = 5 * 3; 
	print("5 * 3 = ".$multiplicacion."<br>");

	/* División */
	$division = 5 / 3; 
	print("5 / 3 = ".$division."<br>");

	/* Modulo */
	$modulo = 5 % 3; 
	print("5 % 3 = ".$modulo."<br>");

?>