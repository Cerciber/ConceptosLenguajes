<?php
	
	/* Asignación */
	$variable = "texto"; 
	print("Asignación = ".$variable."<br>");

	/* Asignación con suma */
	$suma = 5; 
	$suma += 3;
	print("5 + 3 = ".$suma."<br>");

	/* Asignación con resta */
	$resta = 5; 
	$resta -= 3;
	print("5 - 3 = ".$resta."<br>");

	/* Asignación con multiplicación */
	$multiplicacion = 5; 
	$multiplicacion *= 3;
	print("5 * 3 = ".$multiplicacion."<br>");

	/* Asignación con división */
	$division = 5; 
	$division /= 3;
	print("5 / 3 = ".$division."<br>");

	/* Asignación con modulo */
	$modulo = 5; 
	$modulo %= 3;
	print("5 % 3 = ".$modulo."<br>");

	/* Asignación de cadena */
	$modulo = "Texto1"; 
	$modulo .= " Texto2"; 
	print("Texto1 . Texto2 = ".$modulo."<br>"); 

	/* Asignación con incremento */
	$incremento = 5; 
	$incremento++;
	print("5 incremento = ".$incremento."<br>");

	/* Asignación con decremento */
	$decremento = 5; 
	$decremento--;
	print("5 decremento = ".$decremento."<br>");

?>